class Phone:
    def __init__ (self, brand, model):
        self.brand = brand
        self.model = model
       
    def describePhone(self):
        print(f"My dream brand phone is {self.brand}, to be exact it's {self.model}.")
           
class Android(Phone):
    def __init__ (self, brand, model):
        Phone.__init__(self, brand, model)
   
Samsung = Android("Samsung", "S24 Ultra")
Samsung.describePhone() 
